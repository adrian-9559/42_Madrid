int	ft_str_is_alpha(char *arr)
{
	while (*arr)
	{
		if (!((*arr >= 'A' && *arr <= 'Z') || (*arr >= 'a' && *arr <= 'z')))
			return (0);
		arr++;
	}
	return (1);
}
