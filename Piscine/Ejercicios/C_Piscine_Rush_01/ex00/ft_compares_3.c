#include "functions.h"

void	compare_columns_rows_2_3(int arr_p[6][6])
{
	int	i;

	i = 0;
	while (i < 6)
	{
		if (arr_p[0][i] == 2 && arr_p[5][i] == 3)
		{
			arr_p[2][i] = 4;
		}
		if (arr_p[0][i] == 3 && arr_p[5][i] == 2)
		{
			arr_p[3][i] = 4;
		}
		if (arr_p[i][0] == 2 && arr_p[i][5] == 3)
		{
			arr_p[i][2] = 4;
		}
		if (arr_p[i][0] == 3 && arr_p[i][5] == 2)
		{
			arr_p[i][3] = 4;
		}
		i++;
	}
}
