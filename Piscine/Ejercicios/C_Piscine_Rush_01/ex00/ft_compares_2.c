#include "functions.h"

void	compare_columns_rows_1_2(int arr_p[6][6])
{
	int	i;

	i = 0;
	while (i < 6)
	{
		if (arr_p[0][i] == 1 && arr_p[5][i] == 2)
		{
			arr_p[4][i] = 3;
		}
		if (arr_p[0][i] == 2 && arr_p[5][i] == 1)
		{
			arr_p[1][i] = 3;
		}
		if (arr_p[i][0] == 1 && arr_p[i][5] == 2)
		{
			arr_p[i][4] = 3;
		}
		if (arr_p[i][0] == 2 && arr_p[i][5] == 1)
		{
			arr_p[i][1] = 3;
		}
		i++;
	}
}
